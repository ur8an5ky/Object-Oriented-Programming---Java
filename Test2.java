public class Test2 {
    public static void main(String[] args){
        // Char
        char charType = 'a';
        System.out.println(charType);
        // String
        String stringType = "owieczka owieczce owieczkom";
        System.out.println(stringType);
        // Boolean
        boolean boolType = true;
        System.out.println(boolType);

        // Byte
        byte byteType =  Byte.MIN_VALUE;
        byte byteType1 = Byte.MAX_VALUE;
        System.out.println(byteType + " & " + byteType1);
        // Short
        short shortType = Short.MIN_VALUE;
        short shortType1 = Short.MAX_VALUE;
        System.out.println(shortType + " + " + shortType1);
        // Int
        int intType = Integer.MIN_VALUE;
        int intType1 = Integer.MAX_VALUE;
        System.out.println(intType + " + " + intType1);
        // Long
        long longType = Long.MIN_VALUE;
        long longType1 = Long.MAX_VALUE;
        System.out.println(longType + " + " + longType1);

        // Float
        float floatType = 24.12F;
        System.out.println(floatType);
        // Double
        double doubleType = 247.12;
        System.out.println(doubleType);

    }
}